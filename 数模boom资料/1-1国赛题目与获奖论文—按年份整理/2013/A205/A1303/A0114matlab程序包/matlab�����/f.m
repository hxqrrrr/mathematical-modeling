%调用f函数，输入命令ezplot('f(t)',[0,600]);输出时间-队长曲线
%第三问视频1中的排队情况
function m=f(t)
y=0;
for k=0:20
    %时间上限为20
    if t<(30+60*k)&&t>=(60*k)
        y=14+k*1.53-7.15*(t-60*k)/30;
        %14是初始车辆数
    elseif t>=(30+60*k)&&t<(60*k+60)
        y=14+k*1.53-7.15+8.68*(t-30-60*k)/30;
    else y=y;
    end
end
m=y;
end
