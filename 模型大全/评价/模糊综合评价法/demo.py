import numpy as np

# 定义权重集 A
A = np.array([0.2, 0.2, 0.3, 0.2, 0.1])

# 定义模糊关系矩阵 R
R = np.array([
    [0.7, 0.3, 0.0, 0.0, 0.0],
    [0.6, 0.4, 0.0, 0.0, 0.0],
    [0.4, 0.6, 0.0, 0.0, 0.0],
    [0.3, 0.5, 0.2, 0.0, 0.0],
    [0.0, 0.3, 0.7, 0.0, 0.0]
])

# 进行模糊综合评价
B = np.dot(A, R)

print("模糊综合评价结果：", B)

# 计算最终得分
score = np.dot(B, np.array([5, 4, 3, 2, 1]))

print("最终得分：", score)
